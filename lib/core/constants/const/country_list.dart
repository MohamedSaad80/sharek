class CountryModel {
  String? numCode;
  String? alpha2Code;
  String? image;
  String? alpha3Code;
  String? enShortName;
  String? nationality;
  String? dialCode;
  String? regex;
  String? regexwith0;
  NameTranslations? nameTranslations;

  CountryModel(
      {this.numCode,
      this.alpha2Code,
      this.image,
      this.alpha3Code,
      this.enShortName,
      this.nationality,
      this.dialCode,
      this.regex,
      this.regexwith0,
      this.nameTranslations});

  CountryModel.fromJson(Map<String, dynamic> json) {
    numCode = json['num_code'];
    alpha2Code = json['alpha_2_code'];
    image = json['image'];
    alpha3Code = json['alpha_3_code'];
    enShortName = json['en_short_name'];
    nationality = json['nationality'];
    dialCode = json['dial_code'];
    regex = json['regex'];
    regexwith0 = json['regexwith0'];
    nameTranslations = json['nameTranslations'] != null
        ? NameTranslations.fromJson(json['nameTranslations'])
        : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['num_code'] = numCode;
    data['alpha_2_code'] = alpha2Code;
    data['image'] = image;
    data['alpha_3_code'] = alpha3Code;
    data['en_short_name'] = enShortName;
    data['nationality'] = nationality;
    data['dial_code'] = dialCode;
    data['regex'] = regex;
    data['regexwith0'] = regexwith0;
    if (nameTranslations != null) {
      data['nameTranslations'] = nameTranslations!.toJson();
    }
    return data;
  }
}

